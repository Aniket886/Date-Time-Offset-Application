"""Generate PWA icons using Python."""
from pathlib import Path

def create_placeholder_icons():
    """Create placeholder PNG files."""
    sizes = [72, 96, 128, 144, 152, 192, 384, 512]
    output_dir = Path("offset_app/static/pwa")
    output_dir.mkdir(parents=True, exist_ok=True)

    # Create a simple 1x1 transparent PNG
    transparent_png = bytes([
        0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  # PNG signature
        0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,  # IHDR chunk
        0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,  # 1x1
        0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4, 0x89,  # RGBA
        0x00, 0x00, 0x00, 0x0D, 0x49, 0x44, 0x41, 0x54, 0x08, 0xD7,  # IDAT
        0x63, 0xFC, 0xCF, 0xC0, 0x00, 0x00, 0x00, 0x03, 0x00, 0x01, 0x00, 0x05,
        0xFE, 0xD4, 0xA8, 0x22,  # Data
        0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82  # IEND
    ])

    for size in sizes:
        path = output_dir / f"icon-{size}x{size}.png"
        with open(path, "wb") as f:
            f.write(transparent_png)
        print(f"Created: {path}")

if __name__ == "__main__":
    create_placeholder_icons()
